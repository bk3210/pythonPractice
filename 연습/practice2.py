# 신문배달
# 각 층별 구독목록
a = [
    [101, 102, 104, 105, 109],
    [203, 205, 206, 208, 209],
    [301, 302, 303, 304, 305, 306, 307],
    [402, 404, 405, 406, 410]
]

# 구독취소목록
b = [
    [101, 104],
    [205, 206, 208],
    [301, 305],
    [405, 406, 408]
]

#  남은 구독자 목록
# 1차원 배열 : c = [i for i in a if i not in b]
# print(c)
c = []
for i in range(0, len(a)):
    d = [j for j in a[i]
         if j not in b[i]]
    c.append(d)

print(c)